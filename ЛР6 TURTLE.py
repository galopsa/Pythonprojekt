import turtle#Імпортуємо модуль turtle;
turtle.speed(12)#Задамо швидкість черепашці;
turtle.bgcolor('salmon')#Задамо задньому фону колір 'salmon';
turtle.setup(1000,1000)#Задамо розмір вікна для малювання;
turtle.hideturtle()#Сховаємо курсор черепашки;
turtle.pensize(5)#Задамо розмір лінії;
def ramka(): #Створимо рамку, яку залиємо білим кольором, та в майбутньому будем малювати в ній наш малюнок;
	turtle.begin_fill()#Початок заливки;
	turtle.fillcolor('white')#Колір заливки;
	turtle.up()#Підняти черепашку;
	turtle.goto(-360,-390)#Задамо координати;
	turtle.down()#Опустимо череппашку;
	turtle.fd(700)#Намалюєм лінію довжиною в 700 пікселів;
	turtle.left(90)#Розвернемо черепашку в ліво на 90 градусів;
	turtle.fd(700)
	turtle.left(90)
	turtle.fd(700)
	turtle.left(90)
	turtle.fd(700)
	turtle.end_fill()#Кінець заливки;
	turtle.left(90)#Повернемо черепашку в початкове положення;
ramka()
