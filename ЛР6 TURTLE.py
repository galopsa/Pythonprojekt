def prapor(): #Створимо прапор;
	turtle.begin_fill()
	turtle.fillcolor('yellow')
	turtle.fd(40)
	turtle.right(90)
	turtle.fd(10)
	turtle.right(90)
	turtle.fd(40)
	turtle.right(90)
	turtle.fd(10)
	turtle.end_fill()
	turtle.begin_fill()
	turtle.fillcolor('blue')
	turtle.fd(10)
	turtle.right(90)
	turtle.fd(40)
	turtle.right(90)
	turtle.fd(10)
	turtle.right(-90)#Повернемо в початкове положення, щоб запобігти обертанню прапора при кожному нажатті;
	turtle.end_fill()
fon=turtle.Screen() #Створимо новий екран на якому будемо малювати прапори. 
def move(x,y):#Створимо ф-цію для переміщення нашого прапора по заданим координатим;
	turtle.up()
	turtle.goto(x,y)
	turtle.down()
	prapor()
fon.onclick(move)#При нажатті на екран повертає координати (x,y);



